public class CajaFrutas {
    private String frutas;
    private int pesoFrutas;
    public CajaFrutas(String frutas, int pesoFrutas){
        this.frutas=frutas;
        this.pesoFrutas=pesoFrutas;
    }
    public String getFrutas() {
        return frutas;
    }
    public int getPesoFrutas() {
    return pesoFrutas;
    }    
}
